/**
 * Toast POS Sync Endpoint
 *
 * Syncs oyster availability from Toast POS to Firebase. Two entry points:
 *   1. Manual (called by employee.html "Sync Toast" button) — returns list, no writes.
 *   2. Automatic (scheduled) — writes sync'd list to Firebase /menu/serving.
 *
 * Auth:
 *   Uses TOAST_MACHINE_CLIENT OAuth flow. Exchanges client ID + secret for a
 *   bearer token, caches it in module scope, and refreshes before expiry.
 *
 * Mapping strategy:
 *   Toast item names are matched against the local oyster catalog
 *   (`oyster-catalog.json`, regenerated at deploy time from `data/oysters.js`).
 *   Each catalog entry contributes its `id`, `name`, and `aliases[]` to a
 *   normalized lookup index, plus simple plural variants (trailing-s,
 *   ies → y). An item is matched if its normalized name hits any of those keys.
 *
 *   Items that don't match are returned in `unmatched` so the admin UI can
 *   surface them. The opaque GUID-override table in `toast-mapping.js` is the
 *   final escape hatch when even an alias can't capture the discrepancy.
 *
 * Required env vars (see .env.example):
 *   TOAST_API_HOSTNAME       default: https://ws-api.toasttab.com
 *   TOAST_CLIENT_ID          required
 *   TOAST_CLIENT_SECRET      required
 *   TOAST_USER_ACCESS_TYPE   default: TOAST_MACHINE_CLIENT
 *   TOAST_LOCATION_ID        required — Half Shell restaurant GUID
 */

const fs = require('fs');
const path = require('path');
const admin = require('firebase-admin');

// Load .env for local development. Safe no-op in Cloud Functions where env is
// populated by the runtime.
try { require('dotenv').config(); } catch (_) { /* dotenv not installed — fine in prod */ }

const { guidToSlug } = require('./toast-mapping');

// --- Firebase Admin init ---------------------------------------------------
if (!admin.apps.length) {
  admin.initializeApp();
}
const db = admin.database();

// --- Catalog load + match index --------------------------------------------
// `oyster-catalog.json` is generated by `functions/build-catalog.js` from
// `data/oysters.js`. Loaded once at module init; the function instance is
// replaced on each deploy, so the catalog is always fresh.
const CATALOG_PATH = path.join(__dirname, 'oyster-catalog.json');
let CATALOG = [];
try {
  CATALOG = JSON.parse(fs.readFileSync(CATALOG_PATH, 'utf8'));
} catch (err) {
  console.error(`Failed to load ${CATALOG_PATH}: ${err.message}. Did you run \`npm run build:catalog\`?`);
}

function normalize(s) {
  return String(s || '').toLowerCase().replace(/[^a-z0-9]/g, '');
}

/**
 * Generate every normalized key under which a catalog entry can be matched:
 * the entry's id, its display name, every alias, and trailing-s + ies→y
 * plural variants of each. Different normalizations map to the same canonical
 * slug, which is the entry's `id`.
 */
function* matchKeysFor(entry) {
  const sources = [entry.id, entry.name, ...(Array.isArray(entry.aliases) ? entry.aliases : [])];
  for (const source of sources) {
    if (!source) continue;
    const base = normalize(source);
    if (!base) continue;
    yield base;
    // Plural ↔ singular tolerance — let Toast write "Fat Bastards" against
    // catalog "Fat Bastard", and vice versa.
    if (base.endsWith('s')) yield base.slice(0, -1);
    else yield base + 's';
    // English "Blackberries" ↔ "Blackberry" rule.
    if (base.endsWith('ies')) yield base.slice(0, -3) + 'y';
    if (base.endsWith('y')) yield base.slice(0, -1) + 'ies';
  }
}

const matchIndex = new Map(); // normalized key → catalog id (slug)
for (const entry of CATALOG) {
  for (const key of matchKeysFor(entry)) {
    // First write wins: if two catalog entries claim the same key, keep the
    // earlier one. This shouldn't happen in practice; if it does, surface a
    // warning so we know to fix the catalog.
    if (matchIndex.has(key) && matchIndex.get(key) !== entry.id) {
      console.warn(`Catalog ambiguity: key "${key}" maps to both ${matchIndex.get(key)} and ${entry.id}. Keeping ${matchIndex.get(key)}.`);
      continue;
    }
    matchIndex.set(key, entry.id);
  }
}
console.log(`Loaded ${CATALOG.length} catalog oysters with ${matchIndex.size} match keys.`);

// --- Config readers --------------------------------------------------------
function cfg() {
  return {
    hostname: process.env.TOAST_API_HOSTNAME || 'https://ws-api.toasttab.com',
    clientId: process.env.TOAST_CLIENT_ID,
    clientSecret: process.env.TOAST_CLIENT_SECRET,
    userAccessType: process.env.TOAST_USER_ACCESS_TYPE || 'TOAST_MACHINE_CLIENT',
    locationId: process.env.TOAST_LOCATION_ID,
  };
}

function isToastConfigured() {
  const { clientId, clientSecret, locationId } = cfg();
  return !!(clientId && clientSecret && locationId);
}

// --- Token + metadata cache ------------------------------------------------
const tokenCache = { token: null, expiresAt: 0 };
const metadataCache = { lastUpdated: null, matched: null, unmatched: null };
const TOKEN_REFRESH_SKEW_MS = 60_000; // refresh 60s before expiry
const TOAST_RATE_LIMIT_MS = 1100;     // Toast allows 1 req/sec; sleep 1.1s between calls

function sleep(ms) {
  return new Promise(resolve => setTimeout(resolve, ms));
}

async function getToken() {
  const now = Date.now();
  if (tokenCache.token && now < tokenCache.expiresAt - TOKEN_REFRESH_SKEW_MS) {
    return tokenCache.token;
  }

  const { hostname, clientId, clientSecret, userAccessType } = cfg();
  const res = await fetch(`${hostname}/authentication/v1/authentication/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ clientId, clientSecret, userAccessType }),
  });

  if (!res.ok) {
    const text = await res.text().catch(() => '');
    throw new Error(`Toast login failed: ${res.status} ${res.statusText}${text ? ' — ' + text.slice(0, 200) : ''}`);
  }

  const body = await res.json();
  // Toast response shape: { token: { accessToken, expiresIn, tokenType, ... } }
  const accessToken = body?.token?.accessToken || body?.accessToken;
  const expiresIn = body?.token?.expiresIn || body?.expiresIn;
  if (!accessToken) {
    throw new Error(`Unexpected Toast login response shape: ${JSON.stringify(body).slice(0, 200)}`);
  }

  tokenCache.token = accessToken;
  tokenCache.expiresAt = now + ((expiresIn || 900) * 1000);
  return accessToken;
}

// --- Menu fetch ------------------------------------------------------------

/**
 * Fetch published menu metadata. Used for cheap change detection.
 * Returns null if unreachable or empty.
 *
 * Field name note: Menus API v2 returns `lastUpdated` (ISO 8601 UTC). An
 * earlier version of this code read `lastModifiedTime`, which silently never
 * matched and bypassed the cache on every cycle.
 */
async function fetchMenuMetadata() {
  if (!isToastConfigured()) return null;
  const { hostname, locationId } = cfg();
  const token = await getToken();
  const res = await fetch(`${hostname}/menus/v2/metadata`, {
    headers: {
      'Authorization': `Bearer ${token}`,
      'Toast-Restaurant-External-ID': locationId,
    },
  });
  if (!res.ok) return null;
  return res.json();
}

/**
 * Fetch the full published menu with rate-limit respect and 409 retry.
 */
async function fetchMenuRaw(token, retries = 1) {
  const { hostname, locationId } = cfg();
  await sleep(TOAST_RATE_LIMIT_MS); // Toast allows ~1 req/sec
  const res = await fetch(`${hostname}/menus/v2/menus`, {
    headers: {
      'Authorization': `Bearer ${token}`,
      'Toast-Restaurant-External-ID': locationId,
    },
  });

  // 409 = menu was modified mid-request; retry once after a pause.
  if (res.status === 409 && retries > 0) {
    console.log('Menu modified during fetch (409) — retrying...');
    await sleep(2000);
    return fetchMenuRaw(token, retries - 1);
  }
  if (!res.ok) {
    throw new Error(`Menu fetch failed: ${res.status} ${res.statusText}`);
  }
  return res.json();
}

/**
 * Walk the published menu tree (menus → menuGroups → menuItems, recursively
 * descending into nested menuGroups in case Toast ever uses sub-groups).
 */
function* iterateMenuItems(data) {
  const menus = Array.isArray(data) ? data : (data?.menus || []);
  for (const menu of menus) {
    yield* iterateGroups(menu.menuGroups || menu.groups || [], menu.name);
  }
}

function* iterateGroups(groups, menuName) {
  for (const group of groups) {
    for (const item of (group.menuItems || group.items || [])) {
      yield { item, groupName: group.name, menuName };
    }
    if (Array.isArray(group.menuGroups) && group.menuGroups.length) {
      yield* iterateGroups(group.menuGroups, menuName);
    }
  }
}

/**
 * Try to map a Toast item to a catalog slug.
 *  - Primary: normalized name match against the catalog index.
 *  - Override: explicit GUID → slug entry in `toast-mapping.js`.
 * Returns the slug or null.
 */
function matchToastItem(item) {
  // GUID override beats name match for the rare case where staff intentionally
  // want a Toast item to map somewhere unusual.
  if (item.guid && guidToSlug && guidToSlug[item.guid]) {
    return guidToSlug[item.guid];
  }
  const key = normalize(item.name);
  return matchIndex.get(key) || null;
}

/**
 * Fetch oysters from Toast and map to Half Shell catalog slugs.
 * Returns { matched: [slug, ...], unmatched: [{name, guid, groupName, ...}, ...] }.
 *
 * On any failure, returns { matched: [], unmatched: [], error: "..." } and
 * keeps the caller's existing menu state untouched (graceful degradation).
 */
async function fetchFromToastAPI() {
  if (!isToastConfigured()) {
    console.warn('Toast API not configured. Returning empty oyster list.');
    return { matched: [], unmatched: [], error: 'not-configured' };
  }
  if (!CATALOG.length) {
    console.warn('Oyster catalog is empty — name-matching will fail. Run `npm run build:catalog`.');
    return { matched: [], unmatched: [], error: 'catalog-empty' };
  }

  try {
    // Cheap change-detection: if metadata.lastUpdated hasn't moved, skip the
    // full menu fetch and return the previously computed result.
    const metadata = await fetchMenuMetadata();
    const lastUpdated = metadata?.lastUpdated;
    if (
      lastUpdated &&
      lastUpdated === metadataCache.lastUpdated &&
      metadataCache.matched
    ) {
      console.log(`Menu unchanged since ${lastUpdated} — returning cached result (${metadataCache.matched.length} matched, ${metadataCache.unmatched.length} unmatched).`);
      return { matched: metadataCache.matched, unmatched: metadataCache.unmatched };
    }

    const token = await getToken();
    const data = await fetchMenuRaw(token);

    const matched = [];
    const matchedSet = new Set(); // de-dupe — multiple Toast items could map to one slug
    const unmatched = [];

    for (const { item, groupName, menuName } of iterateMenuItems(data)) {
      // Items without a name can't be matched; treat as a data quality issue
      // rather than a hard error.
      if (!item || !item.name) continue;

      const slug = matchToastItem(item);
      if (slug) {
        if (!matchedSet.has(slug)) {
          matched.push(slug);
          matchedSet.add(slug);
        }
      } else {
        // Heuristic for "is this likely an oyster vs a non-oyster Raw Bar item":
        // we don't know for sure without staff input, so we surface everything
        // unmatched and let the admin UI filter. employee.html will eventually
        // let staff dismiss "not an oyster" items so they stop reappearing.
        unmatched.push({
          name: item.name,
          guid: item.guid,
          groupName,
          menuName,
          price: item.price ?? null,
        });
      }
    }

    if (lastUpdated) {
      metadataCache.lastUpdated = lastUpdated;
      metadataCache.matched = matched;
      metadataCache.unmatched = unmatched;
    }

    console.log(`Toast sync: ${matched.length} matched, ${unmatched.length} unmatched (across all menu groups).`);
    return { matched, unmatched };
  } catch (err) {
    console.error('Failed to fetch from Toast API:', err);
    return { matched: [], unmatched: [], error: err.message };
  }
}

/**
 * Fetch oyster IDs from Toast (no Firebase writes).
 * Used by the employee.html "Sync Toast" button for preview before save.
 */
async function fetchToastOysterList() {
  try {
    const { matched, unmatched, error } = await fetchFromToastAPI();
    return {
      success: !error,
      oysterIds: matched,
      count: matched.length,
      unmatched,                     // [{name, guid, groupName, menuName, price}]
      unmatchedCount: unmatched.length,
      configured: isToastConfigured(),
      error: error || undefined,
    };
  } catch (err) {
    console.error('Failed to fetch Toast menu:', err);
    return {
      success: false,
      oysterIds: [],
      count: 0,
      unmatched: [],
      unmatchedCount: 0,
      configured: isToastConfigured(),
      error: err.message,
    };
  }
}

/**
 * Fetch from Toast and write to Firebase /menu/serving.
 * Only called by scheduled/automatic sync, never by the manual preview button.
 */
async function syncToastToFirebase() {
  const now = Date.now();

  try {
    const snapshot = await db.ref('menu/serving').once('value');
    const currentMenu = snapshot.val() || {};

    const { matched: toastOysterIds, unmatched, error } = await fetchFromToastAPI();

    if (error) {
      // Don't touch /menu/serving on a Toast-side failure — preserves whatever
      // was last successfully synced.
      return {
        success: false,
        timestamp: now,
        error,
        note: 'Toast fetch failed; menu not modified',
      };
    }

    if (toastOysterIds.length === 0) {
      // Distinguish "Toast returned an empty menu" from "Toast errored": if we
      // got an empty matched list with no error, that's an actual state we
      // should write through (e.g., off-hours, all sold out). But also surface
      // the unmatched list so the admin UI can flag a possible config issue
      // (e.g., 30 unmatched items but 0 matched probably means catalog drift).
      console.log(`Toast returned 0 matched oysters (${unmatched.length} unmatched).`);
    }

    const updates = {};
    let addCount = 0;
    let updateCount = 0;

    for (const oysterId of toastOysterIds) {
      if (currentMenu[oysterId]) {
        updates[oysterId] = {
          addedAt: currentMenu[oysterId].addedAt,
          lastUpdated: now,
          source: 'toast',
        };
        updateCount++;
      } else {
        updates[oysterId] = {
          addedAt: now,
          lastUpdated: now,
          source: 'toast',
        };
        addCount++;
      }
    }

    // Remove oysters that were on menu (from toast) but are no longer in the
    // Toast list. Only removes items whose source is 'toast' — preserves
    // manual staff additions.
    const toastSet = new Set(toastOysterIds);
    let removeCount = 0;
    for (const [id, entry] of Object.entries(currentMenu)) {
      if (entry && entry.source === 'toast' && !toastSet.has(id)) {
        updates[id] = null;
        removeCount++;
      }
    }

    if (Object.keys(updates).length > 0) {
      await db.ref('menu/serving').update(updates);
    }

    await db.ref('audit-log').push({
      timestamp: now,
      action: 'toast_sync',
      oysterId: null,
      details: `synced_${addCount}_added_${updateCount}_updated_${removeCount}_removed_${unmatched.length}_unmatched`,
      actor: 'Toast API (automatic)',
      readable_timestamp: new Date(now).toISOString(),
    });

    return {
      success: true,
      timestamp: now,
      added: addCount,
      updated: updateCount,
      removed: removeCount,
      total: toastOysterIds.length,
      unmatchedCount: unmatched.length,
    };
  } catch (err) {
    console.error('Sync failed:', err);
    await db.ref('audit-log').push({
      timestamp: now,
      action: 'toast_sync_error',
      oysterId: null,
      details: err.message,
      actor: 'Toast API (automatic)',
      readable_timestamp: new Date(now).toISOString(),
    }).catch(e => console.error('Failed to log error:', e));

    return { success: false, timestamp: now, error: err.message };
  }
}

// --- HTTP handlers ---------------------------------------------------------

async function syncToastMenuHTTP(req, res) {
  try {
    const result = await fetchToastOysterList();
    res.json(result);
  } catch (err) {
    res.status(500).json({ success: false, error: err.message });
  }
}

async function syncToastMenuAutomaticHTTP(req, res) {
  try {
    const result = await syncToastToFirebase();
    res.json(result);
  } catch (err) {
    res.status(500).json({ success: false, error: err.message });
  }
}

